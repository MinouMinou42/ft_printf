#include <wchar.h>

int		ft_unicode_two(wchar_t wc, char *str)
{
	str[0] = (wc >> 6 & 0b00011111) | 0b11000000;
	str[1] = (wc & 0b00111111) | 0b10000000;
	return (2);
}

#include <wchar.h>

int		ft_unicode_three(wchar_t wc, char *str)
{
	str[0] = (wc >> 12 & 0b00001111) | 0b11100000;
	str[1] = (wc >> 6 & 0b00111111) | 0b10000000;
	str[2] = (wc & 0b00111111) | 0b10000000;
	return (3);
}

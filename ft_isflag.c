int			ft_isflag(int c)
{
	char	*flag;

	flag = "#0-+ *$L'hljzqtv0123456789.";
	while (*flag && *flag != (char)c)
		flag++;
	if (*flag == (char)c && *flag != 0)
		return (1);
	return (0);
}

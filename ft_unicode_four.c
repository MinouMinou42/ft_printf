#include <wchar.h>

int		ft_unicode_four(wchar_t wc, char *str)
{
	str[0] = (wc >> 18 & 0b00000111) | 0b11110000;
	str[1] = (wc >> 12 & 0b00111111) | 0b10000000;
	str[2] = (wc >> 6 & 0b00111111) | 0b10000000;
	str[3] = (wc & 0b00111111) | 0b10000000;
	return (4);
}
